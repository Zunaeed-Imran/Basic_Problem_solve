// This program will sum of even and odd number in an array.


let arr = [45, 6, 33, 97, 1, 215, 988, 54, 22];

let n = 0;
let m = 0;

for (let i = 0; i < arr[i]; i++){
   (arr[i] % 2 == 0) 
   n += arr[i];
   document.write(n + "<br>");
}

for (let i = 0; i < arr[i]; i++){
    (arr[i] / 2 != 0) 
     m += arr[i];
     document.write(m + "<br>");
}




